#  flatten() – Convert multi-dimensional array into 1D
# =======================================
# Q2: Given: arr2D = np.array([[10, 20], [30, 40], [50, 60]])
# → Convert this 2D array into a 1D flattened array and print it

import numpy as np

arr2D = np.array([[10, 20], [30, 40], [50, 60]])
print(arr2D.flatten())